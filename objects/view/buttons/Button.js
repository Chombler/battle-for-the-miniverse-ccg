

class Button{
	constructor(color, x, y, width, height, textColor, text, textX, textY){
		this.color = color;
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
		this.textColor = textColor;
		this.text = text;
		this.textX = textX;
		this.textY = textY;
	}
}

module.exports = Button;